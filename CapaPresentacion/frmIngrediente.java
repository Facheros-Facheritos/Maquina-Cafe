    private void btnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerActionPerformed
// Leer el contenido de las cajas de texto
        String valorRecolectado = txtValorRecolectado.getText();
        String nombre = txtNombre.getText();
        String calidad = txtCalidad.getText();
        // Llevar los datos a los atributos del objeto a traves de las propiedades
        ingredientes.setValorRecolectado(valorRecolectado);
        ingredientes.setNombre(nombre);
        ingredientes.setCalidad(calidad);
        // Mostrar el mensaje de confirmaci√≥n
        JOptionPane.showMessageDialog(rootPane,"Datos registrados en el objeto");    }//GEN-LAST:event_btnLeerActionPerformed

